// Str.java
public class Str {
    public static void main(String[] args) {

        System.out.println("Hello, " + "world."); // "Hello, world."

        int sum = 32;
        System.out.println("sum = " + sum); // "sum = 32"

        System.out.println(1 + 2 + "3"); // "33"
        System.out.println("1" + 2 + 3); // "123"
    }
}

